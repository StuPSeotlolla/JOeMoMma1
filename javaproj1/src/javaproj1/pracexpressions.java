package javaproj1;

public class pracexpressions {

	public static void main(String[] args) {
		// expression = operators and operands
		// operands= variables, values, numbers, quantity
		// operators = + - * / %
		
		int friends =  10;
		
		friends = 10 + 1; // or friends++
		friends = 10 - 1; // or friends--
		friends = 10 / 1;
		friends = 10 * 1;
		friends = 10 % 3; // gives remainder from division
		
		System.out.println(friends);
	}

}
